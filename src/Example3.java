public class Example3 {
    //  1 - musbat
    // -3   manfiy
    // 0 na musbat na manfiy
    public static void main(String[] args) {
        int n = 0;
        String text;

        if (n > 0) {
            text = "musbat";
        } else if (n == 0) {
            text = "na musbat na manfiy";
        } else {
            text = "manfiy";
        }


        System.out.println("Siz kiritgan son " + text);

    }
}
