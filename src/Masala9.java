import java.util.Scanner;

public class Masala9 {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int a, b, x, y;
        // a=3 b=5 -> a=5 b=3
        System.out.print("a = ");
        a = scanner.nextInt();

        System.out.print("b = ");
        b = scanner.nextInt();

        x = b;
        System.out.println("a= " + x);

        y = a;
        System.out.println("b= " + y);



    }
}
